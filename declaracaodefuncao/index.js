// como declaro uma função?

function falaoi() { //aqui criei a minha função, essa é a minha declaração classíca de função
    console.log('OI');
}

falaoi() // aqui estou iniciando minha função acima


// arrow function 

// são funções anônimas, abaixo irei mostrar como declaro elas.

const oi = () => { //criei a minha função anônima aqui em digitar a palavra function.
    console.log('Falei oi'); // isso é o que vai mostrar no console.log
};
oi();   //aqui estou inicializando a minha função